
def calculate_total_price(ticket_price, quantity):
    """
    Calculate the total price of tickets based on the ticket price and quantity.

    Parameters:
    item_price (float): The price of a single item.
    quantity (int): The quantity of items to be purchased.

    Returns:
    float: The total price.
    """
    total_price = ticket_price * quantity
    return total_price
